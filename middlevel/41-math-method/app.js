// Math methods
console.log(Math.PI); //عدد پی
console.log(2 ** 3); //توان
console.log(Math.pow(2, 3)); //توان
console.log(Math.sqrt(16)); //جذر
console.log(Math.abs(-16)); //قدرمطلق
console.log(Math.min(20, 43, 65, 98, 54.34));
console.log(Math.max(20, 43, 65, 98, 54.34));
console.log(Math.floor(12.6543)); //حذف قسمت اعشار
console.log(Math.trunc(12.6543)); //حذف قسمت اعشار
console.log(Math.round(12.8))  //گرد کردن
console.log(Math.ceil(12.8))  //بزرگتر
console.log(Math.random() * 100); //عدد رندوم ضرب میکنیم ببیینیم بین کدوم اعداد باشه.